/*
escape sequence: \문자와 함께 사용하는 특수문자
 */

console.log('\0'); // null 문자 
console.log('\b'); //backspace
console.log('\f'); //폼피드, 타자기 종이 한 줄 말아올리기
console.log('\n');//줄바꿈
console.log('\r');//캐리지리턴, 타자기 헤드를 맨 왼쪽으로 옮기기
console.log('\t');//수평 탭
console.log('\v');//수직 탭
console.log('\u0061');//유니코드 문자
console.log('\uc720\uc8fc\ud558');
console.log('\'');//'문자 자체
console.log('\"');//"문자 자체
console.log('\\');// \문자 자체